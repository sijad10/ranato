package pe.gob.sucamec.renagi.jsf.util;

import java.text.SimpleDateFormat;
import javax.faces.component.UIComponent;
import javax.faces.context.FacesContext;
import javax.faces.convert.Converter;
import javax.faces.convert.FacesConverter;

/**
 *
 * @author rmoscoso
 */
@FacesConverter(value = "fechaConverter")

public class FechaConverter implements Converter {

    SimpleDateFormat sdf = new SimpleDateFormat(JsfUtil.bundle("FormatoFecha"));

    @Override
    public Object getAsObject(FacesContext context, UIComponent component, String value) {
        throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
    }

    @Override
    public String getAsString(FacesContext context, UIComponent component, Object value) {
        return value == null ? "" : sdf.format(value);
    }

}
